DATA SEGMENT 
VAL DB 01H,02H,03H,04H,05H
STO DB 5 DUP(0)
DATA ENDS 

CODE SEGMENT 
ASSUME CODE:CS ,DATA:DS
START:
MOV AX,DATA
MOV DS,AX

LEA SI,VAL+04
LEA DI,VAL+04+05
MOV CX,05H

UP:
MOV BL,[SI]
MOV [DI],BL
DEC SI
DEC DI
DEC CX
JNZ UP

MOV AH,4CH
INT 21H
END START 
CODE ENDS
